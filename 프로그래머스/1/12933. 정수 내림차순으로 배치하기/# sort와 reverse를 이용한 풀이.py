# sort와 reverse를 이용한 풀이

# (1) .sort
# 리스트를 정렬하는 파이썬의 내장 함수
# reverse = True: 내림차순 정렬
# reverse = False: 오름차순 정렬 (기본값)
# key: 정렬 기준을 지정
# ex) key=len : 문자열 길이를 기준으로 정렬

# "".join(ls): 정렬된 리스트의 모든 요소를 빈 문자열("")을 구분자로 사용하여 하나의 문자열로 결합

def solution(n):
    ls = list(str(n))
    ls.sort(reverse = True)
    return int("".join(ls))

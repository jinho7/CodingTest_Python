def solution(n):
    # 1 과 == 뿐임
    # 가로 기준 1, 2 뿐
    # dp[i]는 i를 1과 2로 나누는 방법의 수를 저장
    dp = [0] * (n + 1)
    
    dp[0] = 1
    dp[1] = 1
    
    MOD = 1000000007  # 큰 수에 대한 모듈로 연산
    
    # 2부터 n까지 계산
    for i in range(2, n + 1):
        # i-1에 1을 더하는 경우와 i-2에 2를 더하는 경우의 합
        # n = 1 -> 1
        # n = 2 -> 2, 11
        # n = 3 -> (2 1), (11 1) / (1 2)
        # n = 4 -> (2 2), (11 2), (2 1 1), (11 1 1), (1 2 1)
        dp[i] = (dp[i-1] + dp[i-2]) % MOD
    
    return dp[n]